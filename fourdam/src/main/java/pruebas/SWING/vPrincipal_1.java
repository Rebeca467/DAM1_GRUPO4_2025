    public String getNivelEducativoRecomendado(Ruta r) {
        int esfuerzo = r.getNivelEsfuerzo();
        int riesgo = r.getNivelRiesgo();

        int nivel = Math.max(esfuerzo, riesgo);

        return switch (nivel) {
            case 1, 2 ->
                "Primaria";
            case 3 ->
                "Educación Secundaria Obligatoria (ESO)";
            case 4 ->
                "Bachillerato";
            case 5 ->
                "Ciclos Formativos / Adultos";
            default ->
                "Sin especificar";
        };
    }

    public Valoracion crearValoracion(
            int idUsuario,
            int idRuta,
            LocalDate fecha,
            int dificultad,
            int belleza,
            int interes
    ) {
        Usuario usuario = metodos.usuPorId(idUsuario);
        Ruta ruta = metodos.rutaPorId(idRuta);
        Valoracion valoracion = null;
        if (usuario != null && ruta != null) {
            valoracion = new Valoracion(usuario, ruta, fecha, dificultad, belleza, interes);
        } else {
            System.out.println("Usuario o ruta no encontrados.");
        }
        return valoracion;
    }

