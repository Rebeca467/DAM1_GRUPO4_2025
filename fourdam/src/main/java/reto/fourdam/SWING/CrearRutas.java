        fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES); // Puedes cambiar a DIRECTORIES_ONLY si solo quieres carpetas

        int resultado = fileChooser.showOpenDialog(null);

        if (resultado == JFileChooser.APPROVE_OPTION) {
              jTextPane1.setText(fileChooser.getSelectedFile().getAbsolutePath());
        }
